package com.tnsday1;

public class Charac {
	
	public static void main(String args[]) {
		byte bytemax=127;
		byte bytemin=-128;
		
		System.out.println("max range of the byte"+bytemax);
		System.out.println("min range of the byte"+bytemin);
		//same for int ,short ,double
		
		int intmax=2147483647;
		int intmin=-2147483648;
		System.out.println("max range of the int"+intmax);
		System.out.println("min range of the int"+intmin);
		
		
		short shortmax=32767;
		short shortmin=-32768;
		System.out.println("max range of the short"+shortmax);
		System.out.println("min range of the short"+shortmin);
		
//		double doublemax=;
//		double doublemin=;
		
	}

}
